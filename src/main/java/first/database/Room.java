package first.database;

public class Room {

	String id;
	String available;
	int size;
	int floor;
	int prize;
	
	public Room(String id, String available, int size, int floor, int prize) {
		this.id = id;
		this.available = available;
		this.size = size;
		this.floor = floor;
		this.prize = prize;
	}
	
	
}
